public class CustomerChatModel {

    String customer_id;
    String customer_name;
    String customer_mob;
    String ChatContainCustomer;
    String ChatContainAdmin;

    public String getCustomer_id() {
        return customer_id;
    }

    public void setCustomer_id(String customer_id) {
        this.customer_id = customer_id;
    }

    public String getCustomer_name() {
        return customer_name;
    }

    public void setCustomer_name(String customer_name) {
        this.customer_name = customer_name;
    }

    public String getCustomer_mob() {
        return customer_mob;
    }

    public void setCustomer_mob(String customer_mob) {
        this.customer_mob = customer_mob;
    }

    public String getChatContainCustomer() {
        return ChatContainCustomer;
    }

    public void setChatContainCustomer(String chatContainCustomer) {
        ChatContainCustomer = chatContainCustomer;
    }

    public String getChatContainAdmin() {
        return ChatContainAdmin;
    }

    public void setChatContainAdmin(String chatContainAdmin) {
        ChatContainAdmin = chatContainAdmin;
    }


}
